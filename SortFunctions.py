import random

def bubbleSort(arr):
    n = len(arr) 
    for i in range(n):
        for j in range(0, n-i-1):
            if arr[j] > arr[j+1]:
                arr[j], arr[j + 1] = arr[j+1], arr[j]

def insertionSort(arr):
    n = len(arr)
    for i in range(1, n):
        key = arr[i]
        j = i-1
        while j >= 0 and arr[j] > key:
            arr[j+1] = arr[j]
            j -= 1
        arr[j+1] = key

def selectionSort(arr):
    n = len(arr)
    for i in range(n-1):
        min = i
        for j in range(i+1, n):
            if arr[j] < arr[min]:
                min = j
        arr[min], arr[i] = arr[i], arr[min]

def quickSort(arr, l, r):
    if l >= r:
        return
    p, i, j = l, l+1, r
    while j >= i:
        while i <= r and arr[i] <= arr[p]:
            i += 1
        while j > l and arr[j] >= arr[p]:
            j -= 1
        if i > j:
            arr[j], arr[p] = arr[p], arr[j]
        else:
            arr[i], arr[j] = arr[j], arr[i]
    quickSort(arr, l, j-1)
    quickSort(arr, j+1, r)

def bucketSort(arr):
    buckets =  [[] for _ in range(ord('A'), ord('Z')+1)]
    for name in arr:
        if name == "":
            i = 0
        else:
            i = ord(list(name)[0]) % 65
        buckets[i].append(name)
    # sort & merge
    temp = []
    for i in buckets:
        temp += sorted(i)
    return temp

def mergeSort(arr):
    n = len(arr)
    if n == 1:
        return arr

    mid = n // 2
    left = mergeSort(arr[:mid])
    right = mergeSort(arr[mid:])

    temp = []
    i = j = 0

    while i < len(left) and j < len(right):
        if left[i] < right[j]:
            temp.append(left[i])
            i += 1
        else:
            temp.append(right[j])
            j += 1

    temp += left[i:]
    temp += right[j:]

    return temp

def radixSort(arr, index):
    if len(arr) <= 1:
        return arr
    else:
        output = []
        buckets = [[] for _ in range(27)]

        for name in arr:
            if index >= len(name):
                output.append(name)
            else:
                buckets[ord((name[index].lower())) - 96].append(name)

        buckets = [radixSort(item, index + 1) for item in buckets]
        for item in buckets:
            for i in item:
                output += [i]
        return output

def bogoSort(arr):
    n = len(arr)
    sortedArr = True
    for i in range(n-1):
        if arr[i] > arr[i+1]:
            sortedArr = False
            break
    while sortedArr == False:
        random.shuffle(arr)
        sortedArr = True
        for i in range(n-1):
            if arr[i] > arr[i+1]:
                sortedArr = False
                break
            
    return arr

